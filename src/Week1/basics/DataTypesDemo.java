package Week1.basics;

public class DataTypesDemo {
    public static void main(String[] args) {
        // Patient data using different types
        int patientAge = 35;
        double patientWeight = 72.5;
        char bloodGroup = 'A';
        boolean isAdmitted = true;
        long hospitalId = 1234567890L;
        String patientName = "Rahul Sharma";

        System.out.println("Patient Name: " + patientName);
        System.out.println("Age: " + patientAge);
        System.out.println("Weight: " + patientWeight);
        System.out.println("Blood Group: " + bloodGroup);
        System.out.println("Admitted: " + isAdmitted);
        System.out.println("Hospital ID: " + hospitalId);
    }
}
